const localeFromPath = window.location.pathname.split("/").filter(Boolean)[0];
const localeFromQuery = new URLSearchParams(window.location.search).get("lang");
const locale = localeFromPath === "en" || localeFromQuery === "en" ? "en" : "es";
const t = content[locale];
const localizedSlugs = {
  home: { es: "", en: "" },
  services: { es: "servicios", en: "services" },
  story: { es: "historia", en: "history" },
  promos: { es: "promociones", en: "promotions" },
  contact: { es: "contacto", en: "contact" },
  shop: { es: "comprar", en: "shop" }
};
const currentSlug = window.location.pathname.split("/").filter(Boolean)[1] || "";
const currentPage = Object.entries(localizedSlugs).find(([, slugs]) => Object.values(slugs).includes(currentSlug))?.[0] || "home";
const localizedPath = (targetLocale, page = currentPage) => `/${targetLocale}/${localizedSlugs[page][targetLocale]}`.replace(/\/$/, "/");
const canonicalUrl = `https://highwaypharmacypr.com${localizedPath(locale)}`;

document.documentElement.lang = locale;
document.title = t.metaTitle;
document.querySelector('meta[name="description"]').setAttribute("content", t.metaDescription);
document.querySelector('link[rel="canonical"]').setAttribute("href", canonicalUrl);
document.querySelector('meta[property="og:title"]').setAttribute("content", t.metaTitle);
document.querySelector('meta[property="og:description"]').setAttribute("content", t.heroDescription);
document.querySelector('meta[property="og:locale"]').setAttribute("content", locale === "es" ? "es_PR" : "en_US");
document.querySelector('meta[property="og:locale:alternate"]').setAttribute("content", locale === "es" ? "en_US" : "es_PR");
document.querySelector('meta[property="og:url"]').setAttribute("content", canonicalUrl);
document.querySelector('link[hreflang="es"]').setAttribute("href", `https://highwaypharmacypr.com${localizedPath("es")}`);
document.querySelector('link[hreflang="en"]').setAttribute("href", `https://highwaypharmacypr.com${localizedPath("en")}`);
document.querySelector('link[hreflang="x-default"]').setAttribute("href", `https://highwaypharmacypr.com${localizedPath("es")}`);

document.querySelectorAll("[data-copy]").forEach((element) => {
  const value = t[element.dataset.copy];
  if (value !== undefined) element.textContent = value;
});

document.querySelectorAll("[data-icon]").forEach((element) => {
  element.innerHTML = icons[element.dataset.icon] || "";
});

document.querySelectorAll("[data-placeholder]").forEach((element) => {
  element.placeholder = t[element.dataset.placeholder] || "";
});

document.querySelectorAll("[data-alt-copy]").forEach((element) => {
  element.alt = t[element.dataset.altCopy] || "";
});

document.querySelectorAll("[data-fallback-copy]").forEach((element) => {
  element.dataset.fallback = t[element.dataset.fallbackCopy] || "";
});

document.querySelectorAll("[data-nav]").forEach((element) => {
  const key = element.dataset.nav;
  element.textContent = t.nav[key];
  const hashes = {
    home: "",
    services: "#services",
    story: "#story",
    promos: "#promotions",
    contact: "#contact",
    shop: "#shop"
  };
  element.href = `${localizedPath(locale, key)}${hashes[key]}`;
});

document.querySelectorAll("[data-home-link]").forEach((element) => {
  element.href = `/${locale}/`;
});

document.querySelectorAll("[data-services-link]").forEach((element) => {
  element.href = `${localizedPath(locale, "services")}#services`;
});

document.querySelectorAll("[data-lang]").forEach((element) => {
  const target = element.dataset.lang;
  element.classList.toggle("active", target === locale);
  element.href = localizedPath(target);
});

document.querySelector("[data-list='pharmacyList']").innerHTML = t.pharmacyList.map((item) => `<li>${item}</li>`).join("");
document.querySelector("[data-mini-services]").innerHTML = t.miniServices.map((item) => `<span>${item}</span>`).join("");
document.querySelector("[data-values]").innerHTML = t.values.map((item, index) => `${index ? "<i></i>" : ""}<span>${item}</span>`).join("");

const day = new Date().getDay();
const isSunday = day === 0;
const saturday = day === 6;
const hours = isSunday ? t.closed : saturday ? "8:00 AM – 4:45 PM" : "8:00 AM – 6:45 PM";
document.querySelector("#today-hours").textContent = hours;
document.querySelector("#open-status").textContent = isSunday ? t.announcementClosed : t.announcementOpen;
document.querySelector("#announcement-message").textContent = t.announcementMessage;
document.querySelector("#year").textContent = new Date().getFullYear();

const menuButton = document.querySelector(".menu-toggle");
menuButton.addEventListener("click", () => {
  const next = !document.body.classList.contains("menu-open");
  document.body.classList.toggle("menu-open", next);
  menuButton.setAttribute("aria-expanded", String(next));
});

document.querySelectorAll(".site-nav a").forEach((link) => {
  link.addEventListener("click", () => {
    document.body.classList.remove("menu-open");
    menuButton.setAttribute("aria-expanded", "false");
  });
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        revealObserver.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.1 }
);

document.querySelectorAll(".reveal").forEach((element) => revealObserver.observe(element));

if (window.location.hash) {
  window.setTimeout(() => {
    document.querySelector(window.location.hash)?.scrollIntoView();
  }, 80);
}

function handleImageError(image) {
  image.hidden = true;
  image.parentElement?.classList.add("image-failed");
}

function handleLogoError(image) {
  image.closest(".brand")?.classList.add("logo-failed");
}
